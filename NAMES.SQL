--ALL NAME DISPLAY
SELECT FULL_NAME FROM FN;

--FIRST NAME 
SELECT FULL_NAME,SUBSTR(FULL_NAME,1,INSTR(FULL_NAME,' ',1,1)-1) AS FN
FROM FN;

--MIDDLE NAME 
SELECT FULL_NAME,
SUBSTR(FULL_NAME,INSTR(FULL_NAME,' ',1,1)+1, INSTR(FULL_NAME,' ',1,2)-INSTR(FULL_NAME,' ',1,1)-1) AS MN
FROM FN;

--LAST NAME 
SELECT FULL_NAME,SUBSTR(FULL_NAME,INSTR(FULL_NAME,' ',1,2)+1) AS LN
FROM FN;
